    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String nombreUsuario = user.getText();
        String contrase単a = new String(pass.getPassword());
        String SQL_SELECT_USER = "SELECT * FROM usuarios WHERE nombreUsuario =" + "'" + nombreUsuario + "'";
        try {
            conn = Conexion.getConnection();
            stmt = conn.createStatement();
            rs = stmt.executeQuery(SQL_SELECT_USER);
            if (rs.first()) {
                if (rs.getString("nombreUsuario").equals(nombreUsuario)) {
                    String SQL_SELECT = "SELECT * FROM usuarios WHERE nombreUsuario =" + "'" + nombreUsuario + "' AND clave =" + "'" + contrase単a + "'";
                    rs = stmt.executeQuery(SQL_SELECT);
                    if (rs.first()) {
                        UsuarioHelper.buscar(Integer.toString(rs.getInt("id")));
                        frMenu frMenu = new frMenu();
                        frMenu.setVisible(true);
                        dispose();
                    } else {
                        error.setText("Contrase単a no valida");
                    }
                }
            } else {
                error.setText("Usuario no encontrado");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

