    public Object[][] llenarTabla(ArrayList<Horarios> horarioLista) {
        Object fila[][] = null;
        JButton btnFinalizar = new JButton("Finalizar");
        btnFinalizar.setName("Finalizar");
        JButton btnVer = new JButton("Ver Observaciones");
        btnVer.setName("Ver");
        try {
            fila = new Object[horarioLista.size()][6];
            for (int i = 0; i < horarioLista.size(); i++) {
                fila[i][0] = horarioLista.get(i).getId();
                fila[i][1] = PersonalDAO.buscar(String.valueOf(horarioLista.get(i).getIdPersonal())).getNombre();
                fila[i][2] = horarioLista.get(i).getFecha();
                fila[i][3] = horarioLista.get(i).getHoraDesde();
                fila[i][4] = horarioLista.get(i).getHoraHasta();

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return fila;
    }


