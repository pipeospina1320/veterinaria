    private void dateFechaOnSelectionChange(datechooser.events.SelectionChangedEvent evt) {//GEN-FIRST:event_dateFechaOnSelectionChange

        // se instacia el calendario
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(dateFecha.getSelectedDate().getTime());
        // se añade 7 dias
        calendar.add(Calendar.DAY_OF_YEAR, 6);

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String strFechaInicio = sdf.format(dateFecha.getSelectedDate().getTime());
        String strFechaFin = sdf.format(calendar.getTime());

        ArrayList<Horarios> horarioLista = horario.lista("WHERE fecha >=" + "'" + strFechaInicio + "'" + " AND fecha <= " + "'" + strFechaFin + "'");
        String titulo[] = {"id", "id personal", "Nombre", "Fecha", "hora desde", "hora hasta"};
        t.verTable(jTableHorarios, llenarTabla(horarioLista), titulo);
    }//GEN-LAST:event_dateFechaOnSelectionChange

